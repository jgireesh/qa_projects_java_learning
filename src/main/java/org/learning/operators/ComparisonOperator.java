package org.learning.operators;

public class ComparisonOperator {
    public static void main(String[] args) {

        int val1 = 1;
        int val2 = 2;

        if(val1 == val2){
            System.out.println("value 1 = value 2");
        }
        if(val1 != val2){
            System.out.println("value 1 != value 2");
        }
        if(val1 > val2){
            System.out.println("value 1 > value 2");
        }
        if(val1 < val2){
            System.out.println("value 1 < value 2");
        }
    }

}
